package com.pluralsight;

public class Vehicle {

    private int vin; private int year; private String make;
    private String model; private String vehicleType;
    private String color; private int odometer; private double price;

    public Vehicle() {
        this.vin = vin; this.year = year; this.make = make;
        this.model = model; this.vehicleType = vehicleType;
        this.color = color; this.odometer = odometer; this.price = price;
    }


    public double getPrice() {
        return price;
    }
}
